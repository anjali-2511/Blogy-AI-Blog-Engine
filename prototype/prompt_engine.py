def generate_prompt(keyword):
    prompt = f"""
    Write a 1000-1500 word SEO optimized blog on the topic: "{keyword}"

    Requirements:
    - Use engaging title (H1)
    - Use H2 and H3 headings
    - Include keywords naturally
    - Add introduction and conclusion
    - Add FAQ section
    - Add call-to-action (CTA)
    - Ensure readability and SEO optimization
    """

    return prompt